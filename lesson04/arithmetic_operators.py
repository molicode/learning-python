operatingA = 3
operatingB = 2

sum = operatingA + operatingB

print('result of the sum', sum)
print(f'result of the sum {sum}')

subtract = operatingA - operatingB
print(f'result of the subtract {subtract}')

multiplication = operatingA * operatingB
print(f'result of the subtract {multiplication}')

# regresa el resultado de la división en modo punto flotante
division = operatingA / operatingB
print(f'result of the subtract {division}')

# regresa el resultado de la división en modo entero
division = operatingA // operatingB
print(f'result of the subtract {division}')

# the remainder of the division
module = operatingA % operatingB
print(f'result of the subtract {module}')

# exponent operator
exponent = operatingA ** operatingB
print(f'result of the subtract {exponent}')